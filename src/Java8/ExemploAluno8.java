package Java8;

public class ExemploAluno8 {

    private String nome;
    private int alunos;

    public ExemploAluno8(String nome, int alunos){
        this.nome = nome;
        this.alunos = alunos;
    }

    public String getNome() {
        return nome;
    }

    public int getAlunos() {
        return alunos;
    }


}

